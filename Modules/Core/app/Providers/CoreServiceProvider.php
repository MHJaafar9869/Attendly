<?php

namespace Modules\Core\Providers;

use Illuminate\Support\Facades\Blade;
use Illuminate\Support\ServiceProvider;
use Nwidart\Modules\Traits\PathNamespace;
use RecursiveDirectoryIterator;
use RecursiveIteratorIterator;

class CoreServiceProvider extends ServiceProvider
{
    use PathNamespace;

    protected string $name = 'Core';

    protected string $nameLower = 'core';

    /**
     * Boot the application events.
     */
    public function boot(): void
    {
        $this->registerCommands();
        $this->registerCommandSchedules();
        $this->registerTranslations();
        $this->registerConfig();
        $this->registerViews();
        $this->loadMigrationsFrom(module_path($this->name, 'database/migrations'));

        // NEW: Register submodules automatically
        $this->registerSubModules();
    }

    /**
     * Register the service provider.
     */
    public function register(): void
    {
        $this->app->register(EventServiceProvider::class);
        $this->app->register(RouteServiceProvider::class);
    }

    protected function registerSubModules(): void
    {
        $corePath = module_path($this->name);

        // --- Pre-scan subdirectories once ---
        $subDirs = array_filter(glob("$corePath/*"), 'is_dir');

        foreach ($subDirs as $subModulePath) {
            $subModuleName = basename($subModulePath);
            $subModuleNamespace = "Modules\\{$this->name}\\{$subModuleName}";

            $dirs = [
                'migrations'  => "$subModulePath/Database/Migrations",
                'views'       => "$subModulePath/Resources/views",
                'config'      => "$subModulePath/config",
                'routes'      => "$subModulePath/routes",
                'components'  => "$subModulePath/View/Components",
            ];

            // --- Load Migrations ---
            if (is_dir($dirs['migrations'])) {
                $this->loadMigrationsFrom($dirs['migrations']);
            }

            // --- Load Routes ---
            if (is_dir($dirs['routes'])) {
                foreach (glob($dirs['routes'] . '/*.php') as $routeFile) {
                    $this->loadRoutesFrom($routeFile);
                }
            }

            // --- Load Views ---
            if (is_dir($dirs['views'])) {
                $this->loadViewsFrom([$dirs['views']], strtolower($subModuleName));
            }

            // --- Load Config ---
            if (is_dir($dirs['config'])) {
                foreach (glob($dirs['config'] . '/*.php') as $configFile) {
                    $key = strtolower($subModuleName) . '.' . pathinfo($configFile, PATHINFO_FILENAME);
                    $this->mergeConfigFrom($configFile, $key);
                }
            }

            // --- Load Blade components ---
            if (is_dir($dirs['components'])) {
                Blade::componentNamespace("$subModuleNamespace\\View\\Components", strtolower($subModuleName));
            }
        }
    }

    /**
     * Register commands in the format of Command::class
     */
    protected function registerCommands(): void
    {
